abstract class AbstractJob implements JobInterface
{

    /**
     * Start job
     *
     * @return void
     */
    abstract public function start();

    /**
     * Run job
     *
     * @return void
     */
    abstract public function run();

    /**
     * Stop job
     *
     * @return void
     */
    abstract public function stop();

}